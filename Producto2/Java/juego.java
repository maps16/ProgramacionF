public class juego{
    public static void main(String[]args){
	System.out.println("Hola, es hora de jugar ADIVINA EL NUMERO. Primero piensa en un numero entre 1 y 10");

	try{
	    Thread.sleep(5000);
	}

	catch(InterruptedException ex){
	    Thread.currentThread().interrupt();
	}

	System.out.println("Multiplicalo por 9");

	try{
	    Thread.sleep(5000);
	}

	catch(InterruptedException ex){
	    Thread.currentThread().interrupt();
	}

	System.out.println("Si el numero es de dos digitos, sumalos entre si. Por ejemplo si es 25 -> 2+5=7");

	try{
	    Thread.sleep(5000);
	}

	catch(InterruptedException ex){
	    Thread.currentThread().interrupt();
	}

	System.out.println("El numero resultante sumale 4 ");
	try{
	    Thread.sleep(10000);
	}

	catch(InterruptedException ex){
	    Thread.currentThread().interrupt();
	}
	
	System.out.println("Bien. El resultado es 13");
    }
}
